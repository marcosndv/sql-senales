-- vw_Compras_FacturasPendientes
-- Facturas, ND y NC de proveedores genuinamente pendientes de pago:
--   CTACTE > 0 (está en cta cte) Y sin registro de pago en RELACIONESCTASCTES.
-- El ERP no actualiza CTACTE al pagar — el estado real se deduce desde RELACIONESCTASCTES.
-- EMPR0023 no tiene RELACIONESCTASCTES; se incluye solo por CTACTE > 0.

CREATE OR ALTER VIEW dbo.vw_Compras_FacturasPendientes AS

WITH Empresas AS (
    SELECT Empresa, NombreEmpresa FROM dbo.Config_Empresas
)

SELECT
    'EMPR0001'  AS EmpresaId,
    ce.NombreEmpresa,
    lc.CODIGO   AS CodigoFactura,
    lc.PROVEEDOR AS CodigoProveedor,
    e.NOMBRE    AS NombreProveedor,
    e.CUIT,
    lc.COMPROB  AS CodigoComprobante,
    cc.NOMBRE   AS TipoComprobante,
    lc.PTOEMISOR,
    lc.NUMERO,
    RIGHT('0000' + CAST(lc.PTOEMISOR AS nvarchar(10)), 4) + '-'
        + RIGHT('00000000' + CAST(lc.NUMERO AS nvarchar(20)), 8) AS NroComprobante,
    lc.FECHA    AS FechaFactura,
    lc.VENCE    AS FechaVencimiento,
    lc.TOTAL,
    lc.CTACTE   AS SaldoPendiente,
    lc.MONEDA,
    lc.TIPOCAMBIO,
    DATEDIFF(day, lc.VENCE, CAST(GETDATE() AS date)) AS DiasAtraso,
    lc.PERIODO,
    lc.COMENTARIO
FROM EMPR0001.OFICIAL_LIBROCOMPRAS lc
JOIN Empresas ce ON ce.Empresa = 'EMPR0001'
LEFT JOIN EMPR0001.OFICIAL_ENTIDADES e ON e.CODIGO = lc.PROVEEDOR
LEFT JOIN EMPR0001.OFICIAL_CLASECOMPROBANTES cc ON cc.CODIGO = lc.COMPROB
WHERE lc.CTACTE <> 0
  AND NOT EXISTS (
    SELECT 1 FROM EMPR0001.OFICIAL_RELACIONESCTASCTES
    WHERE COMPRA = lc.CODIGO AND FONDO <> '0'
  )

UNION ALL

