    DATEDIFF(day, lc.VENCE, CAST(GETDATE() AS date)), lc.PERIODO, lc.COMENTARIO
FROM EMPR0024.OFICIAL_LIBROCOMPRAS lc
JOIN Empresas ce ON ce.Empresa = 'EMPR0024'
LEFT JOIN EMPR0024.OFICIAL_ENTIDADES e ON e.CODIGO = lc.PROVEEDOR
LEFT JOIN EMPR0024.OFICIAL_CLASECOMPROBANTES cc ON cc.CODIGO = lc.COMPROB
WHERE lc.CTACTE <> 0
  AND NOT EXISTS (SELECT 1 FROM EMPR0024.OFICIAL_RELACIONESCTASCTES WHERE COMPRA = lc.CODIGO AND FONDO <> '0');
