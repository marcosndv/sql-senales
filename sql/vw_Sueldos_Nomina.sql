-- =============================================================================
-- vw_Sueldos_Nomina
-- Nomina consolidada de empleados de las 15 empresas del ERP Senales.
-- Una fila por contrato (un empleado con varios contratos = varias filas).
-- Empleados sin contrato aparecen con EstadoLaboral='SinContrato'.
--
-- Enriquecimientos:
--   - RazonSocial          via dbo.Config_Empresas_Sueldos
--   - UltimoPuesto         ultimo registro en SUELDOS_EMPLEADOSXPUESTOS por empleado
--   - UltimoCentroCosto    ultimo registro en SUELDOS_EMPLEADOSXCENTROCOSTO por empleado
--   - SindicatoNombre      via dbo.Config_Sindicatos (hoy esta vacia, queda preparado)
-- =============================================================================
