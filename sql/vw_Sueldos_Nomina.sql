    e.Empresa,
    ce.NombreEmpresa                                AS RazonSocial,
    e.IdEmpleado,
    c.CONTRATO                                      AS ContratoNro,
    e.CUIL,
    e.NOMBRE                                        AS NombreEmpleado,
    e.FCHNACIM                                      AS FchNacimiento,
    e.FCHANTIG                                      AS FchAntiguedad,
    e.FCHJUBILAC                                    AS FchJubilacion,
    e.SEXO                                          AS SexoCod,
    e.ESTCIVIL                                      AS EstadoCivilCod,
    e.SINDICATO                                     AS SindicatoCod,
    cs.NombreSindicato                              AS SindicatoNombre,
    e.OBRASOCIAL                                    AS ObraSocialCod,
    e.CONDICION                                     AS CondicionCod,
    e.MODALIDAD                                     AS ModalidadCod,
    e.BASICO                                        AS BasicoMaestro,
    c.INGRESO                                       AS FechaIngreso,
    c.EGRESO                                        AS FechaEgreso,
    NULLIF(LTRIM(RTRIM(c.CAUSABAJA)), '')           AS CausaBajaOficial,
    c.PROPORCION                                    AS Proporcion,
    c.PROPORCSAC                                    AS ProporcionSac,
    up.PUESTO                                       AS UltimoPuestoCod,
    pm.PuestoNombre                                 AS UltimoPuestoNombre,
    up.ESTABLECIM                                   AS UltimoEstablecimientoCod,
    up.FECHA                                        AS UltimoPuestoFecha,
    uc.CTROCOSTO                                    AS UltimoCtroCostoCod,
    cm.CtroCostoNombre                              AS UltimoCtroCostoNombre,
    uc.FECHA                                        AS UltimoCtroCostoFecha,
    CASE
        WHEN c.CONTRATO IS NULL                                THEN 'SinContrato'
        WHEN c.EGRESO IS NULL                                  THEN 'Activo'
        WHEN c.EGRESO > CAST(GETDATE() AS date)                THEN 'Activo'
        ELSE                                                       'Baja'
    END                                             AS EstadoLaboral,
    CASE WHEN c.EGRESO IS NOT NULL AND c.EGRESO <= CAST(GETDATE() AS date)
         THEN DATEDIFF(day, c.INGRESO, c.EGRESO)
         WHEN c.INGRESO IS NOT NULL
         THEN DATEDIFF(day, c.INGRESO, CAST(GETDATE() AS date))
         ELSE NULL
    END                                             AS DiasContrato
FROM Empleados e
LEFT JOIN Contratos c
       ON c.Empresa = e.Empresa AND c.IdEmpleado = e.IdEmpleado
LEFT JOIN dbo.Config_Empresas_Sueldos ce
       ON ce.Empresa = e.Empresa
LEFT JOIN dbo.Config_Sindicatos cs
       ON cs.Empresa = e.Empresa AND cs.CodigoSindicato = e.SINDICATO
LEFT JOIN UltimoPuesto up
       ON up.Empresa = e.Empresa AND up.IdEmpleado = e.IdEmpleado AND up.rn = 1
LEFT JOIN PuestosMaestro pm
       ON pm.Empresa = e.Empresa AND pm.PuestoCod = up.PUESTO
LEFT JOIN UltimoCentro uc
       ON uc.Empresa = e.Empresa AND uc.IdEmpleado = e.IdEmpleado AND uc.rn = 1
LEFT JOIN CentrosMaestro cm
       ON cm.Empresa = e.Empresa AND cm.CtroCostoCod = uc.CTROCOSTO;
